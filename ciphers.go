func parseCipherSuite(cipherString string) ([]uint16, error) {
	if cipherString == "" {
		return nil, nil
	}

	ciphers := strings.Split(cipherString, ",")

	var cipherSuites []uint16
	var unknownCiphers []string
	for _, part := range ciphers {
		cipher := strings.TrimSpace(part)
		if cipher == "" {
			continue
		}

		if c, ok := cipherSuiteMap[cipher]; ok {
			cipherSuites = append(cipherSuites, c)
		} else {
			unknownCiphers = append(unknownCiphers, cipher)
		}
	}

	if len(unknownCiphers) > 0 {
		return nil, fmt.Errorf("unknown cipher suites: %s", strings.Join(unknownCiphers, ", "))
	}

	return cipherSuites, nil
}
